/** Normalized property name → curated entry (handles ō/o and case). */
export function lookupCurated(propertyName) {
  if (!propertyName) return null;
  if (CURATED_BY_PROPERTY[propertyName]) return CURATED_BY_PROPERTY[propertyName];
  const norm = propertyName.replace(/ō/gi, 'o').trim();
  for (const [k, v] of Object.entries(CURATED_BY_PROPERTY)) {
    if (k.replace(/ō/gi, 'o').toLowerCase() === norm.toLowerCase()) return v;
  }
  return null;
}
